package edu.brown.cs.student.main;

public class User {
    private String fit;
    private int user_id;
    private int item_id;
    private int rating;
    private String rented_for;
    private String category;
    private int size;
    private int id;

    public String getFit() {
        return fit;
    }

    public int getUser_id() {
        return user_id;
    }

    public int getItem_id() {
        return item_id;
    }

    public int getRating() {
        return rating;
    }

    public String getRented_for() {
        return rented_for;
    }

    public String getCategory() {
        return category;
    }

    public int getSize() {
        return size;
    }

    public int getId() {
        return id;
    }
}
